package Practice;

public class Exercise1_carBrands {
    public static void main(String[] args) {
        //declare and define array
        String[] carBrands = new String[]{"Lamborghini", "Mercedes", "Rolls Royce", "Tesla", "Toyota"};

        //set initializer
        int i = 0;

        //while loop
        while(i < carBrands.length){
            System.out.println(carBrands[i]);

            //incrementation
            i++;

        }//end of loop
    }//end of main
}//end of java class
